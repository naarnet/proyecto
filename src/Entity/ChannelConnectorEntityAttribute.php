namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;
use Doctrine\ORM\Mapping\ManyToOne;
use Doctrine\ORM\Mapping\OneToOne;
use Doctrine\ORM\Mapping\JoinColumn;

/**
 * @ORM\Entity(repositoryClass="App\Repository\ChannelConnectorEntityAttributeRepository")
 */
class ChannelConnectorEntityAttribute
{

    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ManyToOne(targetEntity="ChannelConnector",inversedBy="channelConnector_entity_attribute")
     * @JoinColumn(referencedColumnName="id", nullable=false)
     */
    private $channelConnector;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $attribute_code;
    
    /**
     * @ORM\Column(type="string", length=255)
     */
    private $value;

    public function getId()
    {
        return $this->id;
    }

    public function getMarketplace()
    {
        return $this->marketplace;
    }

    public function setMarketplace($marketplace)
    {
        $this->marketplace = $marketplace;

        return $this;
    }

    public function getAttributeCode()
    {
        return $this->attribute_code;
    }

    public function setAttributeCode($attribute_code)
    {
        $this->attribute_code = $attribute_code;

        return $this;
    }

    public function getValue()
    {
        return $this->value;
    }

    public function setValue($value)
    {
        $this->value = $value;
    }

    /**
     * 
     * @return type
     */
    public function __toString()
    {
        return (string) $this->attribute_code;
    }

}
